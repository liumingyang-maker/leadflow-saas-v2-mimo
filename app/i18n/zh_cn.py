def _template_keys() -> set[str]:
    app_dir = Path(__file__).resolve().parents[1]
    keys: set[str] = set()
    pattern = re.compile(r"""\bt\(\s*(['"])(.*?)\1""")
    for template in (app_dir / "templates").rglob("*.html"):
        if "design_system" in template.parts:
            continue
        keys.update(
            match.group(2) for match in pattern.finditer(template.read_text(encoding="utf-8"))
        )
    return keys


